public class Formules {
/**
 * 
 * @param a le nombre entier qu'on veut le multiplier par 2
 * @return le resultat de la multiplication de a
 */
	public int Doubler(int a)
	{
		return a*2;
	}
	/**
	 * 
	 * @param a l'entier à incrémenter 
	 * @return le resultat (a+1)
	 */
	public int Incrementer(int a)
	{
		return a+1;
	}
	/**
	 * 
	 * @param a  l'entier à decréménter sa valeur 
	 * @return le resultat de la decrementation (a-1)
	 */
	public int Decrementer(int a)
	{
		return a-1;
	}
	
}
